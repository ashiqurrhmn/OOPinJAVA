public class Variable {
    public static void main(String[] args) {

        String name = "Md. Ashiqur Rahman";
        int id = 1226;
        float cg = 3.55f;
        char section = 'F';
        boolean good_student = false;

        System.out.println("String: " + name);
        System.out.println("int: " + id);
        System.out.println("float: " + cg);
        System.out.println("char: " + section);
        System.out.println("boolean: " + good_student);
    }
}
